const dataByMedium = {
  Tamil: tamil,
  Sinhala: sinhala
};

async function runFix() {
  const mongoUri = process.env.MONGODB_URI || process.env.MONGO_URI || 'mongodb://localhost:27017/peerlearn';
  await mongoose.connect(mongoUri);

  let updated = 0;

  for (const medium of ['Tamil', 'Sinhala']) {
    const mediumTag = `medium-${medium.toLowerCase()}`;
    const templates = dataByMedium[medium];

    for (const subject of Object.keys(templates)) {
      for (const type of ['mcq', 'structured', 'essay']) {
        const tpl = templates[subject][type];
        const update = {
          title: `[Grade 9][${medium}][${subject}][${type.toUpperCase()}][${seedTag}]`,
          body: tpl.body,
          correctAnswer: tpl.correctAnswer,
          explanation: tpl.explanation,
          options: type === 'mcq' ? tpl.options : []
        };

        const result = await Question.updateOne(
          { grade, subject, type, tags: { $all: [seedTag, mediumTag] } },
          { $set: update }
        );

        updated += result.modifiedCount || 0;
      }
    }
  }

  console.log(`Grade 9 medium language fix complete. Questions updated: ${updated}`);
  await mongoose.connection.close();
}

runFix().catch(async (error) => {
  console.error('Grade 9 medium language fix failed:', error.message);
  await mongoose.connection.close();
  process.exit(1);
});
